    private void tabMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabMouseClicked
        //String add;//DefaultTableModel table=(DefaultTableModel)tab.getModel();
        try{
        st=cnx.obtenircon().createStatement();
        int row = tab.getSelectedRow();
        String table=(tab.getModel().getValueAt(row,0).toString());
        ResultSet rs=st.executeQuery("select * from produit where idproduit='"+table+"'");
        if(rs.next()){
            id_prod.setText(rs.getString("idproduit"));
            text_des.setText(rs.getString("designation"));
            text_prix.setText(rs.getString("prix_vente"));
            tet_qte_stock.setText(rs.getString("qte"));
        }
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }//GEN-LAST:event_tabMouseClicked

