    void affiche(){
    DefaultTableModel table=(DefaultTableModel)tab.getModel();
        try{
        st=cnx.obtenircon().createStatement();
        ResultSet rs =st.executeQuery("select * from produit");
        while(rs.next()){
        table.addRow(new Object[]{rs.getString("idproduit"),rs.getString("designation"),rs.getString("qte"),rs.getString("prix_achat"),rs.getString("prix_vente")});
        }
    }catch(SQLException ex){
        JOptionPane.showMessageDialog(rootPane,ex);
    }
    }
